let clima = prompt("Ingrese un día")
switch(clima) {
    case "lunes":
     alert ("El lunes estará despejado.");
     break;
   case "martes":
     alert("El martes estará soloeado.");
     break;
   case "miercoles":
    alert ("El miercoles estará con viento.");
     break;
   case "jueves":
     alert ("El jueves estará parcialmente soleado.");
     break;
   case "viernes":
    alert ("El viernes estará parcialmente nublado con probabilidad de lluvia.");
     break;
   case "sabado":
    alert("El sábado estará nublado.");
     break;
   case "domingo":
      alert ("El domingo estará lluvioso.");
      break;
     
   default:
     alert("El día asignado no es correcto");
     
     
   }